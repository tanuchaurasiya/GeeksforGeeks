class Solution {
  public:
    // Function to count paths between two vertices in a directed graph.
    int help(int src, int dst, vector<int> adj[]){
    if(src==dst) return 1;
    int ans=0;
    for(auto child:adj[src])
        ans+=help(child, dst, adj);
    return ans;

    
}
    int countPaths(int V, vector<int> adj[], int src, int dst) {
    return help(src, dst, adj);
    }
};

